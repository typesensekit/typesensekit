import { readFile, writeFile } from "node:fs/promises";
import { operations } from "../packages/core/dist/index.js";

const outputPath = new URL("../docs/api-coverage.md", import.meta.url);
const check = process.argv.includes("--check");

const grouped = Map.groupBy(operations, (operation) => operation.category);
const rows = [...grouped.entries()]
  .sort(([left], [right]) => left.localeCompare(right))
  .map(([category, categoryOperations]) => {
    const names = categoryOperations
      .map((operation) => `\`${operation.name}\``)
      .sort()
      .join("<br>");
    return `| ${category} | ${categoryOperations.length} | ${names} |`;
  })
  .join("\n");

const content = `# TypesenseKit API Coverage

<!-- Generated by scripts/generate-api-coverage.mjs. Do not edit directly. -->

TypesenseKit currently exposes **${operations.length} operations** from its shared registry. The first-class compatibility target is [Typesense v30.2](https://typesense.org/docs/30.2/api/), with the official [Typesense OpenAPI specification](https://github.com/typesense/typesense-api-spec) used as the endpoint reference.

| Category | Count | Operations |
| --- | ---: | --- |
${rows}

## Compatibility Notes

- Global \`synonym_sets.*\` and \`curation_sets.*\` operations target Typesense v30.
- Collection-scoped \`synonyms.*\` and \`overrides.*\` are retained for Typesense v27-v29 compatibility and migrations.
- \`api.call\` remains the escape hatch for endpoints not yet represented by a first-class operation. It is excluded from default read-only MCP mode.
- Route-level tests validate the current HTTP method, path, query, and body contracts for first-class v30 operations.
`;

if (check) {
  const existing = await readFile(outputPath, "utf8").catch(() => "");
  if (existing !== content) {
    console.error(
      "docs/api-coverage.md is stale. Run `pnpm docs:coverage` and commit the result.",
    );
    process.exitCode = 1;
  }
} else {
  await writeFile(outputPath, content);
  console.log("Updated docs/api-coverage.md");
}
